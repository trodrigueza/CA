table = {' ': '11', 'e': '101', 't': '1001', 'o': '10001', 'n': '10000', 'a': '011', 's': '0101', 'i': '01001', 'r': '01000', 'h': '0011', 'd': '00101', 'l': '001001', '!': '001000', 'u': '00011', 'c': '000101', 'f': '000100', 'm': '000011', 'p': '0000101', 'g': '0000100', 'w': '0000011', 'b': '0000010', 'y': '0000001', 'v': '00000001', 'j': '000000001', 'k': '0000000001', 'x': '00000000001', 'q': '000000000001', 'z': '000000000000'}
m = [str(x) for x in input()]
for i in range(len(m)):
    m[i] = table[m[i]]
bin, bins = ''.join(m), []

for i in bin[::8]:
    i = bin[0:8]
    if len(i) != 8:
        while len(i) != 8:
            i = i + '0'
    bin = bin[8:]
    bins.append(hex(int(i, 2))[2:].upper())

for i in range(len(bins)):
    if len(bins[i]) == 1:
        bins[i] = '0' + bins[i] 

print(*bins)
